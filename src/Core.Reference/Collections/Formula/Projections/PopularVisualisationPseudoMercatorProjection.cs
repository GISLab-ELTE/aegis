namespace AEGIS.Reference.Collections.Formula
{
    using System;
    using System.Collections.Generic;

    /// <summary>
    /// Represents the Popular Visualization Pseudo Mercator projection.
    /// </summary>
    /// <author>Tamás Szabó</author>
    [IdentifiedObject("EPSG::1024", "Popular Visualisation Pseudo Mercator")]
    public class PopularVisualisationPseudoMercatorProjection : MercatorProjection
    {
        /// <summary>
        /// Initializes a new instance of the <see cref="PopularVisualisationPseudoMercatorProjection" /> class.
        /// </summary>
        /// <param name="identifier">The identifier.</param>
        /// <param name="name">The name.</param>
        /// <param name="parameters">The parameters of the operation.</param>
        /// <param name="ellipsoid">The ellipsoid.</param>
        /// <param name="areaOfUse">The area of use where the operation is applicable.</param>
        /// <exception cref="System.ArgumentNullException">
        /// The defined operation method requires parameters.
        /// or
        /// The ellipsoid is null.
        /// or
        /// The area of use is null.
        /// </exception>
        public PopularVisualisationPseudoMercatorProjection(String identifier, String name, IDictionary<CoordinateOperationParameter, Object> parameters, Ellipsoid ellipsoid, AreaOfUse areaOfUse)
            : this(identifier, name, null, null, parameters, ellipsoid, areaOfUse)
        {
        }

        /// <summary>
        /// Initializes a new instance of the <see cref="PopularVisualisationPseudoMercatorProjection" /> class.
        /// </summary>
        /// <param name="identifier">The identifier.</param>
        /// <param name="name">The name.</param>
        /// <param name="remarks">The remarks.</param>
        /// <param name="aliases">The aliases.</param>
        /// <param name="parameters">The parameters of the operation.</param>
        /// <param name="ellipsoid">The ellipsoid.</param>
        /// <param name="areaOfUse">The area of use where the operation is applicable.</param>
        /// <exception cref="System.ArgumentNullException">
        /// The defined operation method requires parameters.
        /// or
        /// The ellipsoid is null.
        /// or
        /// The area of use is null.
        /// </exception>
        public PopularVisualisationPseudoMercatorProjection(String identifier, String name, String remarks, String[] aliases, IDictionary<CoordinateOperationParameter, Object> parameters, Ellipsoid ellipsoid, AreaOfUse areaOfUse)
            : base(identifier, name, remarks, aliases, CoordinateOperationMethods.PopularVisualisationPseudoMercatorProjection, parameters, ellipsoid, areaOfUse)
        {
            if (!this.Ellipsoid.IsSphere)
            {
                this.ellipsoidRadius = this.Ellipsoid.RadiusOfConformalSphere((Angle)this.Parameters[CoordinateOperationParameters.LatitudeOfNaturalOrigin]).Value;
            }
        }
    }
}